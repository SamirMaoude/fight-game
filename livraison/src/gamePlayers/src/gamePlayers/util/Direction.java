package gamePlayers.util;

/**
 * Enum representing possible movement directions.
 */
public enum Direction {
    /**
     * Move to the left.
     */
    LEFT,

    /**
     * Move to the right.
     */
    RIGHT,

    /**
     * Move upwards.
     */
    TOP,

    /**
     * Move downwards.
     */
    BOTTOM
}
